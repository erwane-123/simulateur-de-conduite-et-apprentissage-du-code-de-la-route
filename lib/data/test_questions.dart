const int questionsPerSeries = 15;

int getTotalSeriesCount() {
  final totalQuestions = getTestQuestions().length;
  return (totalQuestions / questionsPerSeries).ceil();
}

String getOrganizedQuestionImagePath(int id) {
  final serie = ((id - 1) ~/ questionsPerSeries) + 1;
  final questionInSerie = ((id - 1) % questionsPerSeries) + 1;
  return 'assets/images/questions/series/serie_$serie/question_$questionInSerie.png';
}

String getLegacyQuestionImagePath(int id) {
  return 'assets/images/questions/q$id.png';
}

